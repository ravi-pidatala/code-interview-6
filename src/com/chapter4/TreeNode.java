package com.chapter4;


public class TreeNode {

	private TreeNode left;
	private TreeNode right;

	public TreeNode getLeft() {
		return left;
	}
	
	public void setLeft() {
		this.left = left;
	}

	public TreeNode getRight() {
		return right;
	}
	
	public void setRight() {
		this.right = right;
	}
}
